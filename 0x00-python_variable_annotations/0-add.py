#!/usr/bin/env python3

"""
A type-annotated function.
"""


def add(a: float, b: float) -> float:
    """
    Adds two floating-point numbers and returns their sum.

    Args:
        a (float): The first float number.
        b (float): The second float number.

    Returns:
        float: The sum of 'a' and 'b'.
    """
    return a + b
